    public String getAndSelectRandomPlayer() {
        if (PlayerList.getItemCount()==0) return null;
        Object currPlayer = PlayerList.getItemAt(newRandom.nextInt(PlayerList.getItemCount()));
        PlayerList.setSelectedItem(currPlayer);
        return currPlayer.toString();
    }
    
    public String getRandomPlayer() {
        if (PlayerList.getItemCount()==0) {
            return null;
        }
        return PlayerList.getItemAt(newRandom.nextInt(PlayerList.getItemCount())).toString();
    }
    
    private void updatePlayerCount() {
        PlayerCount.setText("Current Player Count: " + PlayerList.getItemCount());
    }
    
    private void addPlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPlayerActionPerformed
        txtPlayer.setText(txtPlayer.getText().trim());
        if((!isNameInPlayerList(txtPlayer.getText())) && (!"".equals(txtPlayer.getText()))) {
            PlayerList.addItem(txtPlayer.getText());
        }
        txtPlayer.setText("");
        PlayerList.setSelectedIndex(PlayerList.getItemCount()-1);
        updatePlayerCount();
        txtPlayer.requestFocus();
    }//GEN-LAST:event_addPlayerActionPerformed

