    private boolean isNameInPlayerList(String checkName) {
        for(int i=0;i<PlayerList.getItemCount();i++) {
            if(PlayerList.getItemAt(i).toString().compareToIgnoreCase(checkName) == 0)
                return true;
        }
        return false;
    }
    
